# Operadores de Comparação

x = 5
y = 10

x > y
x < y
x == y
x != y

# Operadores de Comparação sempre são Booleanos, ou seja True ou False

print(f'{x} > {y} : {x > y}')  # maior
print(f'{x} < {y} : {x < y}')  # menor
print(f'{x} == {y} : {x == y}')  # igual
print(f'{x} != {y} : {x != y}')  # diferente
print(f'{x} >= {y} : {x >= y}')  # maior ou igual a
print(f'{x} <= {y} : {x <= y}')  # menor ou igual a

